base_path = "content/collaborators"

for name, data in profiles.items():
    for ext in [".md", ".or.md"]:
        filepath = os.path.join(base_path, f"{name}{ext}")
        if not os.path.exists(filepath):
            continue
            
        with open(filepath, 'r', encoding='utf-8') as f:
            content = f.read()
            
        # Replace website
        content = content.replace('website = ""', f'website = "{data["website"]}"')
        
        # Replace body
        parts = content.split('+++\n')
        if len(parts) >= 3:
            frontmatter = parts[0] + '+++\n' + parts[1] + '+++\n\n'
            new_content = frontmatter + data["body"]
            
            with open(filepath, 'w', encoding='utf-8') as f:
                f.write(new_content)
                
print("Successfully batch-updated profiles.")
